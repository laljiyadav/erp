public class TransportBean {

    private String starttime;
    private String stoptime;
    private String drivername;
    private String drivermob;
    private String vehicalbrand;
    private String vehicalno;
    private String route;
    private String destination;
    private String fee;

    public String getStarttime() {
        return starttime;
    }

    public void setStarttime(String starttime) {
        this.starttime = starttime;
    }

    public String getStoptime() {
        return stoptime;
    }

    public void setStoptime(String stoptime) {
        this.stoptime = stoptime;
    }

    public String getDrivername() {
        return drivername;
    }

    public void setDrivername(String drivername) {
        this.drivername = drivername;
    }

    public String getDrivermob() {
        return drivermob;
    }

    public void setDrivermob(String drivermob) {
        this.drivermob = drivermob;
    }

    public String getVehicalbrand() {
        return vehicalbrand;
    }

    public void setVehicalbrand(String vehicalbrand) {
        this.vehicalbrand = vehicalbrand;
    }

    public String getVehicalno() {
        return vehicalno;
    }

    public void setVehicalno(String vehicalno) {
        this.vehicalno = vehicalno;
    }

    public String getRoute() {
        return route;
    }

    public void setRoute(String route) {
        this.route = route;
    }

    public String getDestination() {
        return destination;
    }

    public void setDestination(String destination) {
        this.destination = destination;
    }

    public String getFee() {
        return fee;
    }

    public void setFee(String fee) {
        this.fee = fee;
    }
}
